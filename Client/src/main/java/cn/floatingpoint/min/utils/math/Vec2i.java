public class Vec2i {
    /**
     * The x component of this vector.
     */
    public final int x;

    /**
     * The y component of this vector.
     */
    public final int y;

    public Vec2i(int xIn, int yIn) {
        this.x = xIn;
        this.y = yIn;
    }
}
