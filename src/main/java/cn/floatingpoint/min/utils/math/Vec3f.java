public class Vec3f {
    /**
     * The x component of this vector.
     */
    public final float x;

    /**
     * The y component of this vector.
     */
    public final float y;
    /**
     * The z component of this vector.
     */
    public final float z;

    public Vec3f(float xIn, float yIn, float zIn) {
        this.x = xIn;
        this.y = yIn;
        this.z = zIn;
    }
}
