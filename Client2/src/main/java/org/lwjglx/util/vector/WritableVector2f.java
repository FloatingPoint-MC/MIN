package org.lwjglx.util.vector;

/**
 * Writable interface to Vector2fs
 * 
 * @author $author$
 * @version $revision$ $Id: WritableVector2f.java 3418 2010-09-28 21:11:35Z spasi $
 */
public interface WritableVector2f {

    /**
     * Set the X value
     * 
     * @param x
     */
    void setX(float x);

    /**
     * Set the Y value
     * 
     * @param y
     */
    void setY(float y);

    /**
     * Set the X,Y values
     * 
     * @param x
     * @param y
     */
    void set(float x, float y);
}
